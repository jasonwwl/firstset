impl Texts {
    pub fn feature_title(&self, feature: Feature) -> &'static str {
        match feature {
            Feature::BaseAccess => self.base_access_title,
            Feature::RdsRoles => self.rds_roles_title,
            Feature::RdsLicensing => self.rds_licensing_title,
            Feature::LocalUsers => self.local_users_title,
        }
    }

    pub fn feature_description(&self, feature: Feature) -> &'static str {
        match feature {
            Feature::BaseAccess => self.base_access_description,
            Feature::RdsRoles => self.rds_roles_description,
            Feature::RdsLicensing => self.rds_licensing_description,
            Feature::LocalUsers => self.local_users_description,
        }
    }

    pub fn licensing_mode_label(&self, mode: LicensingMode) -> &'static str {
        match mode {
            LicensingMode::PerDevice => self.per_device,
            LicensingMode::PerUser => self.per_user,
        }
    }

    pub fn cal_method_label(&self, method: CalInstallMethod) -> &'static str {
        match method {
            CalInstallMethod::None => self.no_cal_method,
            CalInstallMethod::Agreement => self.agreement,
            CalInstallMethod::KeyPackId => self.key_pack,
        }
    }
}

pub fn texts(language: Language) -> &'static Texts {
    match language {
        Language::English => &ENGLISH,
        Language::SimplifiedChinese => &SIMPLIFIED_CHINESE,
    }
}

